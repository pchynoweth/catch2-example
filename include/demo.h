namespace demo
{
    /**
     * @brief Calculates the nth Fibonacci number.
     * @param n The index of the Fibonacci number to calculate.
     * @return The nth Fibonacci number.
     */
    int fibonacci(int n);

    /**
     * @brief Calculates the factorial of a number.
     * @param n The number to calculate the factorial of.
     * @return The factorial of n.
     */
    int factorial(int n);

    /**
     * @brief Calculates the greatest common divisor of two numbers.
     * @param a The first number.
     * @param b The second number.
     * @return The greatest common divisor of a and b.
     */
    int gcd(int a, int b);

    /**
     * @brief Calculates the least common multiple of two numbers.
     * @param a The first number.
     * @param b The second number.
     * @return The least common multiple of a and b.
     */
    int lcm(int a, int b);

    /**
     * @brief Checks if a number is prime.
     * @param n The number to check.
     * @return 1 if the number is prime, 0 otherwise.
     */
    int is_prime(int n);

    /**
     * @brief Checks if a number is a palindrome.
     * @param n The number to check.
     * @return 1 if the number is a palindrome, 0 otherwise.
     */
    int is_palindrome(int n);

    /**
     * @brief Checks if a number is an Armstrong number.
     * @param n The number to check.
     * @return 1 if the number is an Armstrong number, 0 otherwise.
     */
    int is_armstrong(int n);

    /**
     * @brief Checks if a number is a perfect number.
     * @param n The number to check.
     * @return 1 if the number is a perfect number, 0 otherwise.
     */
    int is_perfect(int n);

    /**
     * @brief Checks if a number is a strong number.
     * @param n The number to check.
     * @return 1 if the number is a strong number, 0 otherwise.
     */
    int is_strong(int n);

    /**
     * @brief Checks if a number is a Harshad number.
     * @param n The number to check.
     * @return 1 if the number is a Harshad number, 0 otherwise.
     */
    int is_harshad(int n);

    /**
     * @brief Checks if a number is a Disarium number.
     * @param n The number to check.
     * @return 1 if the number is a Disarium number, 0 otherwise.
     */
    int is_disarium(int n);

    /**
     * @brief Checks if a number is a Neon number.
     * @param n The number to check.
     * @return 1 if the number is a Neon number, 0 otherwise.
     */
    int is_neon(int n);

    /**
     * @brief Checks if a number is a Pronic number.
     * @param n The number to check.
     * @return 1 if the number is a Pronic number, 0 otherwise.
     */
    int is_pronic(int n);

    /**
     * @brief Checks if a number is an Automorphic number.
     * @param n The number to check.
     * @return 1 if the number is an Automorphic number, 0 otherwise.
     */
    int is_automorphic(int n);
}